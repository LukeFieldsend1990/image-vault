function navItemsForRole(role: Role) {
  if (isIndustryRole(role)) return LICENSEE_NAV;
  if (role === "rep") return REP_NAV;
  return TALENT_NAV; // talent, admin
}

function NavItem({ item, active, alert }: { item: { href: string; label: string; icon: React.ReactNode }; active: boolean; alert?: boolean }) {
  return (
    <Link
      href={item.href}
      className="flex items-center gap-3 rounded px-3 py-2.5 text-sm transition relative"
      style={{
        color: active ? "#ffffff" : "var(--color-sidebar-muted)",
        background: active ? "rgba(192,57,43,0.18)" : "transparent",
      }}
    >
      {active && (
        <span
          className="absolute left-0 top-1/2 -translate-y-1/2 w-[3px] rounded-r"
          style={{ height: "60%", background: "var(--color-accent)" }}
        />
      )}
      <span className="relative" style={{ color: active ? "var(--color-accent)" : "inherit" }}>
        {item.icon}
        {alert && (
          <span
            className="absolute -top-1 -right-1 w-2 h-2 rounded-full"
            style={{ background: "#c0392b" }}
          />
        )}
      </span>
      {item.label}
    </Link>
  );
}

