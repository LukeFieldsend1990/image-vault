function navItemsForRole(role: Role) {
  if (isComplianceRole(role)) return COMPLIANCE_NAV;
  if (isIndustryRole(role)) return LICENSEE_NAV;
  if (role === "rep") return REP_NAV;
  return TALENT_NAV; // talent, admin
}

