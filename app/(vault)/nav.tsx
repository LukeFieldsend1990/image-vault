export function NavLinks({ role, industryOrgType, pipelineEnabled, royaltyMeterEnabled, inboundEnabled, licenceAlert, complianceEnabled, platformOversight, insurerWatcher, unionWatcher, agencyMember }: { role: Role; email?: string; industryOrgType?: OrgType | null; pipelineEnabled?: boolean; royaltyMeterEnabled?: boolean; inboundEnabled?: boolean; licenceAlert?: boolean; complianceEnabled?: boolean; platformOversight?: boolean; insurerWatcher?: boolean; unionWatcher?: boolean; agencyMember?: boolean }) {
  const pathname = usePathname();
  let base = navItemsForRole(role);
  // Agents (reps belonging to a talent agency) get an Agency surface, placed
  // directly under their Roster.
  if (role === "rep" && agencyMember && !base.some((item) => item.href === "/agency")) {
    base = [base[0], AGENCY_NAV_ITEM, ...base.slice(1)];
  }
  if (!inboundEnabled) {
    base = base.filter((item) => item.href !== "/inbox");
  }
  if (complianceEnabled === false) {
    base = base.filter((item) => !item.href.startsWith("/compliance"));
  }
  // The oversight Productions tracker + repeat-offender Scorecard are only for
  // compliance watchers holding a platform-wide grant; hide them for scoped
  // watchers. The Watchlist is per-union, so any union watcher (platform- or
  // union-scoped) sees their own list.
  if (isComplianceRole(role) && !platformOversight) {
    base = base.filter((item) => item.href !== "/productions" && item.href !== "/oversight");
  }
  if (isComplianceRole(role) && !platformOversight && !unionWatcher) {
    base = base.filter((item) => item.href !== "/watchlist");
  }
  // The Member roster and Team management are union-owned (one list per union). Only
  // a union watcher maintains them — a platform-wide regulator has no union.
  if (isComplianceRole(role) && !unionWatcher) {
    base = base.filter((item) => item.href !== "/members" && item.href !== "/union-team");
  }
  // Insurer watchers (compliance role with an insurer grant) get the Underwriting
  // surface, placed first as their primary landing.
  if (isComplianceRole(role) && insurerWatcher && !base.some((item) => item.href === "/underwriting")) {
    base = [UNDERWRITING_NAV_ITEM, ...base];
  }
  if (role === "talent" && !royaltyMeterEnabled) {
    base = base.filter((item) => item.href !== "/royalties" && item.href !== "/vault/monitor");
  }
  // Scan-service (capture) and dubbing companies: receiving/processing only —
  // directory browsing, licence management, and render bridge are not relevant.
  if (isIndustryRole(role) && (industryOrgType === "scan_service" || industryOrgType === "dubbing")) {
    base = base.filter((item) => item.href !== "/directory" && item.href !== "/licences" && item.href !== "/bridge");
  }
  // VFX vendors: receiving only — the transfers section describes outbound
  // dispatch workflows that don't apply to them.
  if (isIndustryRole(role) && industryOrgType === "vfx_vendor") {
    base = base.filter((item) => item.href !== "/transfers");
  }
  const items = role === "talent" && pipelineEnabled
    ? [...base.slice(0, -1), PIPELINE_NAV_ITEM, base[base.length - 1]]
    : base;

  const LICENCE_HREFS = new Set(["/licences", "/vault/licences"]);

  return (
    <nav className="space-y-0.5 px-3">
      {items.map((item) => {
        const active = pathname === item.href || pathname.startsWith(item.href + "/");
        const alert = licenceAlert && LICENCE_HREFS.has(item.href);
        return <NavItem key={item.href} item={item} active={active} alert={alert} />;
      })}
    </nav>
  );
}
